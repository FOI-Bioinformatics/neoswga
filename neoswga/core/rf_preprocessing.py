def predict_with_enhanced_features(primer_list, fg_genome_sequence, conditions=None,
                                   primer_positions=None, model_path=None):
    """
    Predict primer scores using enhanced 120+ feature model.

    Uses advanced feature engineering including thermodynamics with
    reaction conditions, secondary structure, spatial features,
    sequence complexity, and binding landscape analysis.

    Args:
        primer_list: List of primer sequences
        fg_genome_sequence: Target genome sequence string
        conditions: ReactionConditions object (or use defaults)
        primer_positions: Optional dict of {primer: {'forward': [...], 'reverse': [...]}}
        model_path: Path to enhanced model file (or use default)

    Returns:
        DataFrame with predictions and features
    """
    # Import advanced features module
    from neoswga.core.advanced_features import AdvancedFeatureEngineer
    from neoswga.core.reaction_conditions import ReactionConditions

    if conditions is None:
        conditions = ReactionConditions(temp=30.0, na_conc=50.0)

    # Engineer features
    logger.info(f"Engineering enhanced features for {len(primer_list)} primers...")
    feature_engineer = AdvancedFeatureEngineer(
        fg_genome_sequence, conditions, primer_positions or {}
    )
    features_df = feature_engineer.engineer_features(primer_list)
    logger.info(f"Generated {len(features_df.columns)} features")

    # Load enhanced model
    if model_path is None:
        model_dir = os.path.join(os.path.dirname(__file__), 'models')
        model_path = os.path.join(model_dir, 'enhanced_rf_model.pkl')

    if not os.path.exists(model_path):
        logger.warning(f"Enhanced model not found at {model_path}")
        logger.warning("Falling back to standard model. Run scripts/train_enhanced_rf.py first.")
        return None

    # Load model and feature list
    # SECURE: Use load_model_safely with hash verification
    try:
        model_data = load_model_safely(model_path, verify_hash=False)  # Enhanced models may vary
    except Exception as e:
        logger.warning(f"Failed to load enhanced model: {e}")
        return None

    model = model_data['model']
    feature_names = model_data['feature_names']
    logger.info(f"Loaded enhanced model with {len(feature_names)} features")

    # Prepare features for prediction
    # Ensure all required features are present
    missing_features = set(feature_names) - set(features_df.columns)
    if missing_features:
        logger.warning(f"Missing {len(missing_features)} features, filling with zeros")
        for feat in missing_features:
            features_df[feat] = 0.0

    # Select features in correct order
    X = features_df[feature_names].copy()

    # Handle missing/infinite values
    X = X.replace([np.inf, -np.inf], np.nan)
    X = X.fillna(0)

    # Predict
    predictions = model.predict(X)

    # Build output DataFrame
    output_df = features_df.copy()
    output_df['enhanced_prediction'] = predictions

    # Convert log predictions back to linear scale if needed
    if 'log_enrichment' in str(model_data.get('metadata', {}).get('target_variable', '')):
        output_df['predicted_enrichment'] = 10 ** predictions
    else:
        output_df['predicted_enrichment'] = predictions

    logger.info(f"Enhanced prediction complete")
    logger.info(f"Prediction range: [{predictions.min():.3f}, {predictions.max():.3f}]")

    return output_df


def predict_new_primers_enhanced(df=None, primer_list=None, fg_genome_sequence=None,
                                 conditions=None, primer_positions=None,
                                 use_enhanced=True, model_path=None):
    """
    Unified prediction function supporting both standard and enhanced models.

    Args:
        df: Standard feature DataFrame (for standard mode)
        primer_list: List of primers (for enhanced mode)
        fg_genome_sequence: Genome sequence (for enhanced mode)
        conditions: Reaction conditions (for enhanced mode)
        primer_positions: Position dict (for enhanced mode)
        use_enhanced: Whether to use enhanced features
        model_path: Optional custom model path

    Returns:
        DataFrame with predictions
    """
    if use_enhanced:
        if primer_list is None:
            raise ValueError("primer_list required for enhanced mode")
        if fg_genome_sequence is None:
            raise ValueError("fg_genome_sequence required for enhanced mode")

        result = predict_with_enhanced_features(
            primer_list, fg_genome_sequence, conditions,
            primer_positions, model_path
        )

        if result is not None:
            return result

        # Fall through to standard mode if enhanced model not available
        logger.info("Falling back to standard prediction mode")

    # Standard prediction
    if df is None:
        raise ValueError("df required for standard prediction mode")
    return predict_new_primers(df)


