# Known SHA-256 hashes of trusted model files
# Update these when retraining models
_TRUSTED_MODEL_HASHES = {
    'random_forest_filter.p': '7320e265744b4449353e404f7f0240f3918026a31d619d2c235a422c12c54e76',
}

# Set to False to disable hash verification (not recommended for production)
_VERIFY_MODEL_HASHES = True


class ModelIntegrityError(Exception):
    """Raised when model file hash verification fails."""
    pass


def _compute_file_hash(filepath: str) -> str:
    """Compute SHA-256 hash of a file."""
    sha256_hash = hashlib.sha256()
    with open(filepath, "rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            sha256_hash.update(chunk)
    return sha256_hash.hexdigest()


def load_model_safely(model_path: str, verify_hash: bool = None) -> object:
    """
    Load a pickle model file with optional hash verification.

    This function provides protection against pickle deserialization attacks
    by verifying the model file hash before loading.

    Args:
        model_path: Path to the model file.
        verify_hash: Whether to verify hash. Defaults to _VERIFY_MODEL_HASHES.

    Returns:
        The unpickled model object.

    Raises:
        FileNotFoundError: If model file doesn't exist.
        ModelIntegrityError: If hash verification fails.
    """
    if verify_hash is None:
        verify_hash = _VERIFY_MODEL_HASHES

    if not os.path.exists(model_path):
        raise FileNotFoundError(f"Model file not found: {model_path}")

    model_name = os.path.basename(model_path)

    if verify_hash and model_name in _TRUSTED_MODEL_HASHES:
        expected_hash = _TRUSTED_MODEL_HASHES[model_name]
        actual_hash = _compute_file_hash(model_path)

        if actual_hash != expected_hash:
            raise ModelIntegrityError(
                f"Model file hash mismatch for {model_name}.\n"
                f"Expected: {expected_hash}\n"
                f"Actual:   {actual_hash}\n"
                f"The model file may have been corrupted or tampered with.\n"
                f"To update the hash after retraining, update _TRUSTED_MODEL_HASHES "
                f"in rf_preprocessing.py"
            )
        logging.getLogger(__name__).debug(
            f"Model hash verified: {model_name}"
        )
    elif verify_hash:
        logging.getLogger(__name__).warning(
            f"No trusted hash for model: {model_name}. Loading without verification."
        )

    with open(model_path, 'rb') as f:
        return pickle.load(f)


# Module-level k-mer cache for performance
# Structure: {(fname_prefix, k): {kmer: count}}
_kmer_cache: Dict[Tuple[str, int], Dict[str, int]] = {}

# Worker-shared data (populated by initializer, shared via fork)
_worker_fnames: List[str] = []
_worker_k: int = 0


def _init_kmer_worker(fnames: List[str], k: int, sampling_enabled: bool = False,
                      sample_rate: float = 0.1, min_count_threshold: int = 5) -> None:
    """
    Worker initializer to preload k-mer files and set sampling parameters.

    This function runs once when each worker starts, loading k-mer data
    into the module-level cache and setting sampling parameters. This eliminates
    redundant file loading since each worker loads files only once instead of per-primer.

    Expected speedup: 35-45% for Step 3 scoring (more with sampling enabled).

    Args:
        fnames: List of file prefixes for k-mer files
        k: K-mer length to load
        sampling_enabled: Whether k-mer sampling is enabled
        sample_rate: Fraction of k-mers to sample (0.01-1.0)
        min_count_threshold: Always include k-mers with count >= this
    """
    global _kmer_cache, _worker_fnames, _worker_k
    global _sampling_enabled, _sample_rate, _min_count_threshold

    _worker_fnames = fnames
    _worker_k = k

    # Set sampling parameters in worker
    _sampling_enabled = sampling_enabled
    _sample_rate = sample_rate
    _min_count_threshold = min_count_threshold

    # Preload k-mer files into cache for this worker
    for fname_prefix in fnames:
        cache_key = (fname_prefix, k)
        if cache_key not in _kmer_cache:
            fpath = f"{fname_prefix}_{k}mer_all.txt"
            if os.path.exists(fpath):
                _kmer_cache[cache_key] = get_kmer_to_count_dict(fpath)
                logger.debug(f"Worker loaded {fpath}: {len(_kmer_cache[cache_key]):,} k-mers")
            else:
                _kmer_cache[cache_key] = {}
                logger.warning(f"K-mer file not found: {fpath}")

# Fix for sklearn >= 1.0 compatibility with models pickled on older versions
# Several internal modules were renamed with underscore prefix in sklearn 1.0
def _fix_sklearn_module_aliases():
    """Add module aliases for sklearn modules renamed in version 1.0+."""
    module_mappings = [
        ('sklearn.ensemble.forest', 'sklearn.ensemble._forest'),
        ('sklearn.ensemble.weight_boosting', 'sklearn.ensemble._weight_boosting'),
        ('sklearn.tree.tree', 'sklearn.tree._classes'),
        ('sklearn.neighbors.typedefs', 'sklearn.neighbors._typedefs'),
        ('sklearn.utils.weight_vector', 'sklearn.utils._weight_vector'),
    ]
    for old_name, new_name in module_mappings:
        try:
            # Import the new module location
            parts = new_name.split('.')
            mod = __import__(new_name)
            for part in parts[1:]:
                mod = getattr(mod, part)
            sys.modules[old_name] = mod
        except (ImportError, AttributeError):
            pass  # Module not available or already exists

_fix_sklearn_module_aliases()

